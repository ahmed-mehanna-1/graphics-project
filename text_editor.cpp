void makeRasterFont(void)
{
    GLuint i;
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    BigfontOffset = glGenLists (128);
    for (i = 32; i < 127; i++) {
        glNewList(i+BigfontOffset, GL_COMPILE);
            //glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, rasters[i-32]);
            glBitmap(16, 26, 0.0, 4.0, 18.0, 0.0, BigRasters[i-32]);
        glEndList();
    }


    SmallfontOffset = glGenLists (128);
    for (i = 32; i < 127; i++) {
        glNewList(i+SmallfontOffset, GL_COMPILE);
            glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, smallRasters[i-32]);
        glEndList();
    }
}


